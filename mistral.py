class MixingHead(nn.Module):
    def __init__(self, config):
        super().__init__()
        self.config = config
        self.hidden_size = config.hidden_size
        self.intermediate_size = config.intermediate_size
        self.gate_proj = nn.Linear(self.hidden_size*2, self.intermediate_size, bias=False)
        self.up_proj = nn.Linear(self.hidden_size*2, self.intermediate_size, bias=False)
        self.down_proj = nn.Linear(self.intermediate_size, self.hidden_size, bias=False)
        self.act_fn = ACT2FN[config.hidden_act]

    def forward(self, hidden_states, thought_hidden_states):
        gate = self.act_fn(self.gate_proj(torch.cat([hidden_states, thought_hidden_states], dim=-1)))
        up = self.up_proj(torch.cat([hidden_states, thought_hidden_states], dim=-1))
        down = self.down_proj(up)
        return hidden_states * gate + down



